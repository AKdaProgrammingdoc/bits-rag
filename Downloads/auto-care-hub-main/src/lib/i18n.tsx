type Key = keyof typeof dict.en;

const Ctx = createContext<{ lang: Lang; t: (k: Key) => string; toggle: () => void }>({
  lang: "en",
  t: (k) => dict.en[k],
  toggle: () => {},
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>("en");
  useEffect(() => {
    const saved = (typeof window !== "undefined" && localStorage.getItem("lang")) as Lang | null;
    if (saved === "ta" || saved === "en") setLang(saved);
  }, []);
  const toggle = () => {
    setLang((l) => {
      const next = l === "en" ? "ta" : "en";
      if (typeof window !== "undefined") localStorage.setItem("lang", next);
      return next;
    });
  };
  const t = (k: Key) => dict[lang][k] ?? dict.en[k];
  return <Ctx.Provider value={{ lang, t, toggle }}>{children}</Ctx.Provider>;
}

export const useI18n = () => useContext(Ctx);
